package day5class;

public class Task13 {
	public static void main(String[] args) {
		int num = 3;
		switch (num) {
		case 1:
			System.out.println("IS MONDAY");
			break;
		case 2:
			System.out.println("IS TUESDAY");
			break;
		case 3:
			System.out.println("IS WEDNESDAY");
			break;
		case 4:
			System.out.println("IS THURSDAY");
			break;
		default:
			System.out.println("IS INVALID");
		}
	}
}
